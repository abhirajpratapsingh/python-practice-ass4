# Q9 : write a python script to calculate the volume of a cuboid
take_lenth = int ( input ( "enter the lenth of cuboid :" ) )
take_width = int ( input ( "enter the width of cuboid :" ) )
take_hight = int ( input ( "enter the height of cuboid :" ) )
volume = take_hight * take_lenth * take_width
print ( "the volume of cuboid is" , volume )